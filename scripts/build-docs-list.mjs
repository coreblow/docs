#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";

const root = process.cwd();
const docsDir = path.join(root, "docs");
const outputPath = path.join(root, "src", "docs.generated.js");
const docsConfigPath = path.join(docsDir, "docs.json");

if (!fs.existsSync(docsDir)) {
  console.error("build-docs-list: missing docs directory");
  process.exit(1);
}

const docs = walk(docsDir)
  .filter((file) => /\.mdx?$/i.test(file))
  .filter((file) => !isGeneratedTranslatedDoc(path.relative(docsDir, file)))
  .map((file) => buildRecord(file))
  .sort((a, b) => a.path.localeCompare(b.path));
const docsConfig = fs.existsSync(docsConfigPath)
  ? JSON.parse(fs.readFileSync(docsConfigPath, "utf8"))
  : {};

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(
  outputPath,
  `// Generated by scripts/build-docs-list.mjs. Do not edit by hand.
export const DOCS = ${JSON.stringify(docs, null, 2)};
export const DOCS_CONFIG = ${JSON.stringify(docsConfig, null, 2)};
`,
);

console.log(`Docs list built: ${docs.length} documents -> ${path.relative(root, outputPath)}`);

function walk(dir) {
  const files = [];
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    if (entry.name.startsWith(".")) continue;
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...walk(full));
    } else if (entry.isFile()) {
      files.push(full);
    }
  }
  return files;
}

function isGeneratedTranslatedDoc(relPath) {
  const firstSegment = normalize(relPath).split("/")[0];
  return new Set(["zh-CN", "ja-JP", "ko-KR"]).has(firstSegment);
}

function buildRecord(file) {
  const rel = normalize(path.relative(docsDir, file));
  const raw = fs.readFileSync(file, "utf8");
  const body = stripFrontMatter(raw).trim();
  const title = extractTitle(body) ?? titleFromPath(rel);
  const route = routeFromPath(rel);
  const section = rel.includes("/") ? rel.split("/")[0] : "overview";
  const summary = extractSummary(body);
  return {
    title,
    route,
    path: `docs/${rel}`,
    section,
    summary,
    content: body,
  };
}

function normalize(value) {
  return value.replace(/\\/g, "/");
}

function routeFromPath(rel) {
  const withoutExt = rel.replace(/\.(md|mdx)$/i, "");
  if (withoutExt === "index") return "/";
  if (withoutExt.endsWith("/index")) return `/${withoutExt.slice(0, -"/index".length)}`;
  return `/${withoutExt}`;
}

function stripFrontMatter(value) {
  if (!value.startsWith("---")) return value;
  const end = value.indexOf("\n---", 3);
  if (end === -1) return value;
  return value.slice(end + 4);
}

function extractTitle(value) {
  const match = value.match(/^#\s+(.+)$/m);
  return match?.[1]?.trim();
}

function extractSummary(value) {
  const paragraphs = value
    .replace(/^# .+$/m, "")
    .split(/\n{2,}/)
    .map((item) => item.replace(/\s+/g, " ").trim())
    .filter((item) => item && !item.startsWith("#") && !item.startsWith("```"));
  return paragraphs[0]?.slice(0, 220) ?? "";
}

function titleFromPath(rel) {
  const base = rel.replace(/\.(md|mdx)$/i, "").split("/").pop() ?? "Docs";
  return base
    .split("-")
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join(" ");
}
